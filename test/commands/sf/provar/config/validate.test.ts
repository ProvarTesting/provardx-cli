import { TestContext } from '@salesforce/core/lib/testSetup';
import { expect } from 'chai';
import { stubSfCommandUx } from '@salesforce/sf-plugins-core';
import SfProvarConfigValidate from '..\\..\\..\\..\\..\\src\\commands\\sf\\provar\\config\\validate';

describe('sf provar config validate', () => {
  const $$ = new TestContext();
  let sfCommandStubs: ReturnType<typeof stubSfCommandUx>;

  beforeEach(() => {
    sfCommandStubs = stubSfCommandUx($$.SANDBOX);
  });

  afterEach(() => {
    $$.restore();
  });

  it('runs hello', async () => {
    await SfProvarConfigValidate.run([]);
    const output = sfCommandStubs.log
      .getCalls()
      .flatMap((c) => c.args)
      .join('\n');
    expect(output).to.include('hello world');
  });

  it('runs hello with --json and no provided name', async () => {
    const result = await SfProvarConfigValidate.run([]);
    expect(result.path).to.equal('D:\\provardx-cli\\src\\commands\\sf\\provar\\config\\validate.ts');
  });

  it('runs hello world --name Astro', async () => {
    await SfProvarConfigValidate.run(['--name', 'Astro']);
    const output = sfCommandStubs.log
      .getCalls()
      .flatMap((c) => c.args)
      .join('\n');
    expect(output).to.include('hello Astro');
  });
});
